        <br>
        <p class="text-rosliny">Wilgotność na początku zbiorów wynosiła zwykle 25-28 proc. Można było jednak zaobserwować pod tym względem ogromne zróżnicowanie i to nie spowodowane warunkami pogodowymi (np. większymi opadami deszczu), ale przede wszystkim różnicami w odmianach. Dobrze było to widać na przykład podczas Dnia Kukurydzy w Skrzelewie (6 października), gdzie zebrano 14 odmian kukurydzy (tej samej firmy) i na bieżąco podawano jej wilgotność. Okazało się, że kształtowała się ona na poziomie od 23,3 proc. do 35 proc.</p>
